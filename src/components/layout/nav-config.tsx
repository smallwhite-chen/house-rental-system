/** 判斷 path 是否落在某 NavItem 的 active 範圍。 */
export function isNavItemActive(item: NavItem, pathname: string): boolean {
  if (item.match) {
    return pathname === item.match || pathname.startsWith(`${item.match}/`);
  }
  return pathname === item.href;
}
